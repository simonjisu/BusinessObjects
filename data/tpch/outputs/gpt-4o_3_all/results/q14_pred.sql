SELECT (SUM(CASE WHEN P.P_TYPE LIKE '%PROMO%' THEN L.L_EXTENDEDPRICE * (1 - L.L_DISCOUNT) ELSE 0 END) * 100.0 / SUM(L.L_EXTENDEDPRICE * (1 - L.L_DISCOUNT))) AS promo_revenue_percentage
FROM LINEITEM L
JOIN PART P ON L.L_PARTKEY = P.P_PARTKEY
WHERE L.L_SHIPDATE >= '1993-09-01' AND L.L_SHIPDATE < '1993-10-01';