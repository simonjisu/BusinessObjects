SELECT (SUM(L.L_EXTENDEDPRICE * (1 - L.L_DISCOUNT)) / (SELECT SUM(L2.L_EXTENDEDPRICE * (1 - L2.L_DISCOUNT)) FROM LINEITEM L2 JOIN ORDERS O2 ON L2.L_ORDERKEY = O2.O_ORDERKEY WHERE O2.O_ORDERDATE >= '1993-09-01' AND O2.O_ORDERDATE < '1993-10-01')) * 100 AS promo_revenue_percentage
FROM LINEITEM L
JOIN PARTSUPP PS ON L.L_PARTKEY = PS.PS_PARTKEY AND L.L_SUPPKEY = PS.PS_SUPPKEY
JOIN PART P ON PS.PS_PARTKEY = P.P_PARTKEY
JOIN ORDERS O ON L.L_ORDERKEY = O.O_ORDERKEY
WHERE P.P_TYPE LIKE 'PROMO' AND O.O_ORDERDATE >= '1993-09-01' AND O.O_ORDERDATE < '1993-10-01';