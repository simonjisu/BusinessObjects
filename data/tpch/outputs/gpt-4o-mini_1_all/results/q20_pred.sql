SELECT COUNT(DISTINCT S.S_SUPPKEY) AS SupplierCount
FROM SUPPLIER S
JOIN PARTSUPP PS ON S.S_SUPPKEY = PS.PS_SUPPKEY
JOIN LINEITEM L ON PS.PS_PARTKEY = L.L_PARTKEY AND PS.PS_SUPPKEY = L.L_SUPPKEY
WHERE S.S_NATIONKEY = (SELECT N.N_NATIONKEY FROM NATION N WHERE N.N_NAME = 'CANADA')
AND L.L_SHIPDATE BETWEEN '1993-01-01' AND '1993-12-31'
AND PS.PS_AVAILQTY > 0.5 * L.L_QUANTITY;