SELECT S.S_SUPPKEY, COUNT(L.L_ORDERKEY) AS LateDeliveries
FROM SUPPLIER S
JOIN PARTSUPP PS ON S.S_SUPPKEY = PS.PS_SUPPKEY
JOIN LINEITEM L ON PS.PS_PARTKEY = L.L_PARTKEY AND PS.PS_SUPPKEY = L.L_SUPPKEY
WHERE S.S_NATIONKEY = (SELECT N.N_NATIONKEY FROM NATION N WHERE N.N_NAME = 'GERMANY')
AND L.L_COMMITDATE < L.L_RECEIPTDATE
GROUP BY S.S_SUPPKEY
ORDER BY LateDeliveries DESC
LIMIT 100;