SELECT T2.S_SUPPKEY, SUM(T1.L_EXTENDEDPRICE * (1 - T1.L_DISCOUNT)) AS total_revenue
FROM LINEITEM T1
JOIN SUPPLIER T2 ON T1.L_SUPPKEY = T2.S_SUPPKEY
JOIN PARTSUPP T3 ON T1.L_PARTKEY = T3.PS_PARTKEY AND T1.L_SUPPKEY = T3.PS_SUPPKEY
WHERE T1.L_SHIPDATE BETWEEN '1993-03-01' AND '1993-05-31'
GROUP BY T2.S_SUPPKEY
ORDER BY total_revenue DESC
LIMIT 1;