SELECT (promo_revenue / total_revenue) * 100 AS promo_percentage
FROM (
    SELECT SUM(L_EXTENDEDPRICE * (1 - L_DISCOUNT)) AS total_revenue
    FROM LINEITEM
    JOIN ORDERS ON LINEITEM.L_ORDERKEY = ORDERS.O_ORDERKEY
    WHERE ORDERS.O_ORDERDATE >= '1993-09-01' AND ORDERS.O_ORDERDATE < '1993-10-01'
) AS total,
(
    SELECT SUM(L_EXTENDEDPRICE * (1 - L_DISCOUNT)) AS promo_revenue
    FROM LINEITEM
    JOIN PART ON LINEITEM.L_PARTKEY = PART.P_PARTKEY
    JOIN ORDERS ON LINEITEM.L_ORDERKEY = ORDERS.O_ORDERKEY
    WHERE PART.P_TYPE LIKE '%PROMO%'
    AND ORDERS.O_ORDERDATE >= '1993-09-01' AND ORDERS.O_ORDERDATE < '1993-10-01'
) AS promo;